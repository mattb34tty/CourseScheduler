    public void rebuildSemesterComboBoxes()
    {
        ArrayList<String> semesters = SemesterQueries.getSemesterList();
        currentSemesterComboBox.setModel(new javax.swing.DefaultComboBoxModel(semesters.toArray()));
        if (semesters.size() > 0)
        {
            currentSemesterLabel.setText(semesters.get(0));
            currentSemester = semesters.get(0);
        }
        else
        {
            currentSemesterLabel.setText("None, add a semester.");
            currentSemester = "None";
        }
    }
    
    public void rebuildCourseComboBoxes()
    {
        ArrayList<String> courseCodes = CourseQueries.getAllCourseCodes(currentSemester);
        Iterator it = courseCodes.iterator();
        courseIDComboBox.removeAllItems();
        adminDisplayCourseListComboBox.removeAllItems();
        adminDropCourseComboBox.removeAllItems();
        while(it.hasNext()){
            String nextCourse = it.next().toString();
            courseIDComboBox.addItem(nextCourse);
            adminDisplayCourseListComboBox.addItem(nextCourse);
            adminDropCourseComboBox.addItem(nextCourse);
        }
    }
    
    
    public void rebuildStudentComboBoxes()
    {
        ArrayList<StudentEntry> students = StudentQueries.getAllStudents();
        Iterator it = students.iterator();
        studentSchedulerComboBox.removeAllItems();
        studentDisplayScheduleComboBox.removeAllItems();
        adminDropStudentComboBox.removeAllItems();
        studentDropCourseStudentComboBox.removeAllItems();
        while (it.hasNext()){
            String name = it.next().toString();
            studentSchedulerComboBox.addItem(name);
            studentDisplayScheduleComboBox.addItem(name);
            adminDropStudentComboBox.addItem(name);
            studentDropCourseStudentComboBox.addItem(name);
        }
    }
    
    
    public void rebuildStudentDropCourseCourseComboBox()
    {
        ArrayList<ScheduleEntry> schedule = ScheduleQueries.getScheduleByStudent(currentSemester,dropCourseStudentID);
        studentDropCourseCourseComboBox.removeAllItems();
        for(ScheduleEntry sched:schedule){
            String courseCode = sched.getCourseCode();
            studentDropCourseCourseComboBox.addItem(courseCode);
        }
    }

