    private void adminDropStudentButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_adminDropStudentButtonActionPerformed
        // TODO add your handling code here:
        ArrayList<String> semesters = new ArrayList<String>();
        semesters=SemesterQueries.getSemesterList();
        int studentIndex = adminDropStudentComboBox.getSelectedIndex();
        ArrayList<StudentEntry> students = StudentQueries.getAllStudents();
        ArrayList<String> semesterOutput = new ArrayList<String>();
        
        String studentID = students.get(studentIndex).getStudentID();
        
        for(String semester:semesters){
        
            ArrayList<ScheduleEntry> classes = ScheduleQueries.getScheduleByStudent(semester,studentID);
            ArrayList<String> courseCodes = new ArrayList<String>();
            for(ScheduleEntry entry:classes){
                String cID = entry.getCourseCode();
                courseCodes.add(cID);
            }
        
            String semesterTitle = String.format("\nFor %s:\n",semester);
            semesterOutput.add(semesterTitle);
        
            for(String course:courseCodes){
                ScheduleQueries.dropStudentScheduleByCourse(semester,studentID,course);
                String newOutput = String.format("%s has been dropped from %s.\n", StudentQueries.getStudent(studentID),course);
                semesterOutput.add(newOutput);
                String upgradeStudent = ScheduleQueries.UpgradeWaitlistScheduleEntry(semester,course);
                if (upgradeStudent.isEmpty()==false){
                    newOutput = String.format("%s has been scheduled into %s in above's place.\n", StudentQueries.getStudent(upgradeStudent),course);
                    semesterOutput.add(newOutput);
                }

            }
        }
        adminDropStudentTextArea.selectAll();
        adminDropStudentTextArea.replaceSelection("");
        for (String line:semesterOutput){
            adminDropStudentTextArea.append(line);
        }
        StudentQueries.dropStudent(studentID);
        rebuildStudentComboBoxes();
    }//GEN-LAST:event_adminDropStudentButtonActionPerformed

